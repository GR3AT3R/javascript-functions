// Start coding here
let calculator = {
    add: (a, b) => a + b,
    subtract: (a, b) => a - b,
    multiply: (a, b) => a * b,
    divide: (a, b) => a / b,
};

let addResult = calculator.add(10, 20);
console.log(addResult);

let subtractResult = calculator.subtract(1000, 300);
console.log(subtractResult);

let multiplyResult = calculator.multiply(100, 20);
console.log(multiplyResult);

let divideResult = calculator.divide(3000, 10);
console.log(divideResult);